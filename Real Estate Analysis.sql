-- Create the database
CREATE DATABASE RealEstateAgencyDB;

-- Switch to the new database
USE RealEstateAgencyDB;

-- Supprimer la table si elle existe
IF OBJECT_ID('dbo.Agents', 'U') IS NOT NULL
    DROP TABLE dbo.Agents;
-- AGENTS TABLE
CREATE TABLE Agents (
    AgentID INT PRIMARY KEY,
    FirstName NVARCHAR(50) NOT NULL,
    LastName NVARCHAR(50) NOT NULL,
    Phone NVARCHAR(50),
    Email NVARCHAR(100) UNIQUE
);


IF OBJECT_ID('dbo.Customers', 'U') IS NOT NULL
    DROP TABLE dbo.Customers;

-- CUSTOMERS TABLE
CREATE TABLE Customers (
    CustomerID INT PRIMARY KEY,
    FirstName NVARCHAR(50) NOT NULL,
    LastName NVARCHAR(50) NOT NULL,
    Phone NVARCHAR(50),
    Email NVARCHAR(100)
    
);

-- Supprimer la table si elle existe
IF OBJECT_ID('dbo.Properties', 'U') IS NOT NULL
    DROP TABLE dbo.Properties;

-- PROPERTIES TABLE
CREATE TABLE Properties (
    PropertyID INT PRIMARY KEY,
    PropertyType NVARCHAR(50), -- Apartment, House, Office, etc.
    Location NVARCHAR(50),
    Size_sqm DECIMAL(10,2),
    PriceUSD DECIMAL(15,2)
 
);

-- SALES TABLE
CREATE TABLE Sales (
    SaleID INT PRIMARY KEY,
    PropertyID INT NOT NULL,
    CustomerID INT NOT NULL,
    AgentID INT NOT NULL,
    SaleDate DATE,
    SalePrice DECIMAL(15,2),
    CONSTRAINT FK_Sales_Properties FOREIGN KEY (PropertyID)
        REFERENCES Properties(PropertyID)
        ON UPDATE CASCADE
        ON DELETE NO ACTION,
    CONSTRAINT FK_Sales_Customers FOREIGN KEY (CustomerID)
        REFERENCES Customers(CustomerID)
        ON UPDATE CASCADE
        ON DELETE NO ACTION,
    CONSTRAINT FK_Sales_Agents FOREIGN KEY (AgentID)
        REFERENCES Agents(AgentID)
        ON UPDATE CASCADE
        ON DELETE NO ACTION
);

-- VISITS TABLE
CREATE TABLE Visits (
    VisitID INT PRIMARY KEY,
    CustomerID INT NOT NULL,
    PropertyID INT NOT NULL,
    AgentID INT NOT NULL,
    VisitDate DATE,
    CONSTRAINT FK_Visits_Properties FOREIGN KEY (PropertyID)
        REFERENCES Properties(PropertyID)
        ON UPDATE CASCADE
        ON DELETE NO ACTION,
    CONSTRAINT FK_Visits_Customers FOREIGN KEY (CustomerID)
        REFERENCES Customers(CustomerID)
        ON UPDATE CASCADE
        ON DELETE NO ACTION,
    CONSTRAINT FK_Visits_Agents FOREIGN KEY (AgentID)
        REFERENCES Agents(AgentID)
        ON UPDATE CASCADE
        ON DELETE NO ACTION
);

